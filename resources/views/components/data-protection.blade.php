    <h2>Protection des données à caractère personnel</h2>
    <div class="overflow-y-auto h-64 border p-4 rounded-md text-sm markdown">
        <p>L’Université libre de Bruxelles – avenue Roosevelt 50 1050 Bruxelles – ci-après « l’ULB » est le responsable
            du
            traitement des données à caractère personnel vous concernant collectées et traitées dans le cadre de votre
            utilisation d’Infofin.</p>
        <p><strong>Création de compte pour les externes à l’ULB</strong><br/>
            L’utilisation d’Infofin requiert la création d’un compte qui constitue donc un contrat entre vous et
            l’Université. Vous pouvez supprimer ce compte à tout moment (en écrivant à XXX ou via la procédure prévue
            sur le
            site XXX) ce qui entrainera la fin de l’accès aux ressources Infofin et la suppression définitive de vos
            données
            personnelles.</p>
        <p><strong>Utilisation par les membres de l’ULB</strong><br/>
            En tant que membres du personnel de l’ULB vous pouvez utiliser les services d’Infofin en vous connectant à
            l’aide de votre ULBID et en suivant la procédure MFA (ou en utilisant votre mot de passe le cas échéant).
            Vous
            pourrez alors choisir de recevoir une newsletter et déterminer vos intérêts spécifiques. Ces choix peuvent
            être
            modifiés à tout moment en vous connectant à votre profil d’utilisateur.</p>
        <p><strong>Inscription à la Newsletter</strong><br/>
            Si vous choisissez de vous inscrire à la newsletter d’Infofin votre email sera uniquement traité pour
            l’envoi de
            la newsletter et ceci jusqu’à votre désinscription. Vous pouvez vous désinscrire à tout moment via votre
            profil
            utilisateur ou en cliquant sur le lien de désinscription qui accompagnera chaque communication. Vos centres
            d’intérêts peuvent être modifiés à tout moment en vous connectant à votre profil utilisateur. La suppression
            de
            votre compte entrainera également l’arrêt de l’abonnement à la Newsletter.</p>
        <p><strong>Conformément au Règlement (UE) 2016/679 du Parlement européen et du Conseil du 27 avril 2016 relatif
                à la
                protection des personnes physiques à l’égard du traitement des données à caractère personnel et à la
                libre
                circulation de ces données : le Règlement général sur la protection des données ci-après (RGPD)</strong>,
            toutes les données relatives aux utilisateurs d’Infofin ne sont détenues que par l’ULB qui assure les
            mesures de
            sécurité adéquates afin d’en garantir l’intégrité et la confidentialité. Elles ne sont pas partagées avec
            des
            tiers. Toutes les personnes accédant à ces données sont tenues au respect de la confidentialité. Les données
            sont conservées jusqu’à la suppression du compte par l’utilisateur ou après XXX d’inactivité du compte
            (aucune
            connexion) si aucune inscription à la newsletter n’est programmée.</p>
        <p>Le Délégué à la protection des données de l’Université peut être contacté pour toute question relative à la
            protection des données à caractère personnel ou pour toute demande d’exercice de droits (accès rectification
            effacement portabilité et limitation de traitement) à l’adresse suivante : <a href="mailto:rgpd@ulb.be"
                                                                                          class="text-blue-500 underline">rgpd@ulb.be</a>
            (Avenue F. Roosevelt 50 CP 130 1050 Bruxelles).</p>
        <p>Si vous estimez que notre réponse ne respecte pas vos droits vous pouvez adresser une plainte à l’Autorité de
            protection des données (<a href="https://www.autoriteprotectiondonnees.be" class="text-blue-500 underline">Introduire
                une plainte | Autorité de protection des données</a>).</p>
    </div>
    <p class="text-sm mt-4">En vous inscrivant, vous prenez conscience que vous acceptez nos conditions générales et la
        politique de protection des données.</p>
