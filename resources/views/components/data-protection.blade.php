    <h2>Protection des données à caractère personnel</h2>
    <div class="overflow-y-auto h-64 border p-4 rounded-md text-sm markdown">
        <p>L’Université libre de Bruxelles – avenue Roosevelt 50, 1050 Bruxelles –, ci-après « l’ULB », est le
            responsable du traitement des données à caractère personnel vous concernant collectées et traitées dans le
            cadre de votre utilisation d’Infofin.</p>
        <p><strong>Utilisation par des externes à l’ULB</strong><br/>
            L’utilisation d’Infofin requiert la création d’un compte ULB. Les informations quant au traitement de vos
            données dans ce cadre sont fournies lors de la procédure de création. Une fois le compte créé, vous pourrez
            utiliser Infofin comme tout membre de l’ULB. Si vous demandez la suppression de votre compte, vous perdrez
            l’accès à Infofin.</p>
        <p><strong>Utilisation par les membres de l’ULB</strong><br/>
            En tant que membres du personnel de l’ULB, vous pouvez utiliser les services d’Infofin en vous connectant à
            l’aide de votre ULBID et en suivant la procédure MFA (ou en utilisant votre mot de passe le cas échéant).
        </p>
        <p><strong>Services Infofin</strong><br/>
            Lors de votre première connexion à Infofin, vous déterminerez vos intérêts spécifiques. Votre adresse email
            sera utilisée pour vous communiquer les informations relatives aux financements liés à vos centres
            d’intérêts. Ces choix peuvent être modifiés à tout moment en vous connectant à votre profil d’utilisateur.

            Vous pouvez également vous désinscrire de l’envoi d’email à tout moment via votre profil utilisateur ou en
            cliquant sur le lien de désinscription qui accompagnera chaque communication. Cette désinscription ne
            remettra pas en cause votre accès à la base de données. En revanche, si vous décidez de supprimer votre
            compte vous ne recevrez plus d’email et ne serez plus en mesure de voir les offres.
        </p>
        <p><strong>Conformément au Règlement (UE) 2016/679 du Parlement européen et du Conseil du 27 avril 2016 relatif
                à la protection des personnes physiques à l’égard du traitement des données à caractère personnel et à
                la libre circulation de ces données : le Règlement général sur la protection des données, ci-après
                (RGPD), toutes les données relatives aux utilisateurs d’Infofin ne sont détenues que par l’ULB, qui
                assure les mesures de sécurité adéquates afin d’en garantir l’intégrité et la confidentialité. Elles ne
                sont pas partagées avec des tiers. Toutes les personnes accédant à ces données sont tenues au respect de
                la confidentialité. Les données relatives au compte sont conservées jusqu’à la suppression de celui-ci
                par l’utilisateur . Les données d’utilisation d’Infofin sont conservées un an.</p>
        <p>Le Délégué à la protection des données de l’Université peut être contacté pour toute question relative à la
            protection des données à caractère personnel ou pour toute demande d’exercice de droits (accès,
            rectification, effacement, portabilité et limitation de traitement), à l’adresse suivante :<a
                href="mailto:rgpd@ulb.be"
                class="text-blue-500 underline">rgpd@ulb.be</a>
            (Avenue F. Roosevelt 50 CP 130 1050 Bruxelles).</p>
        <p>Si vous estimez que notre réponse ne respecte pas vos droits vous pouvez adresser une plainte à l’Autorité de
            protection des données (<a href="https://www.autoriteprotectiondonnees.be" class="text-blue-500 underline">Introduire
                une plainte | Autorité de protection des données</a>).</p>
    </div>
    <p class="text-sm mt-4">En continuant, vous prenez conscience que vous acceptez nos conditions générales et la
        politique de protection des données.</p>
